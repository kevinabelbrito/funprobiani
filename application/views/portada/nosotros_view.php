        <h1 class="text-center title">Acerca de Funprobiani</h1>
        <p>La Fundación en Pro del Bienestar Animal, es una Fundación sin Fines de Lucro fundada en el año 2007. La misma se dedica al rescate, recuperación, esterilización y entrega en adopción de animales callejeros en la ciudad de Maturín. En vista de que la problemática de los animales abandonados radica fundamentalmente en su sobrepoblación, FUNPROBIANI está activamente involucrada en el control poblacional por medio de la esterilización masiva de perros y gatos.</p>
        <p>FUNPROBIANI financia sus operaciones por medio de modestas donaciones privadas y jornadas de vacunación y desparasitación. Sin embargo, esta atención se ofrece a muy bajo (o ningún) costo, por lo que estos ingresos resultan insuficientes para el mantenimiento de los animales y para las campañas de esterilización que el cumplimiento de sus objetivos requiere. El principal apoyo recibido hasta el momento, ha sido aportado por una importante cadena de ferreterías quien ha contribuido con alimento para los animales, sin embargo esta ayuda ha cesado en la actualidad.</p>
        <p>Lamentablemente, la falta de vehículo y la escasez de recursos han impedido a FUNPROBIANI hasta el momento, brindar la atención que la población de animales callejeros de los demás Municipios requiere, concentrando sus esfuerzos de rescate en Maturín y sus inmediaciones. Los rescates de animales son en efecto realizados por los propios miembros de FUNPROBIANI en sus vehículos privados.</p>
        <p>Los miembros de FUNPROBIANI trabajan de manera voluntaria, siendo el único personal remunerado eventualmente: cuatro médicos veterinarios quienes se han abocado a la causa.</p>
        <p>FUNPROBIANI fue protocolizada en el Registro Principal del Estado Monagas según documento nro. 28, folios 222/229, Tomo 1, Protocolo 1, cuarto trimestre año 2009, en fecha 22 de Octubre del 2009.</p>
