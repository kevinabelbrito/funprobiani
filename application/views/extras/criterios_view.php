            <h2 class="text-center title">Criterios Generales</h2>
            <p>La diferencia clave entre la conservación y el bienestar animal es que la primera se enfoca en las especies, poblaciones y hábitats, mientras que el bienestar se enfoca en el animal individual.</p>
            <p>El bienestar animal está definido tanto por el estado físico y psicológico de un animal como por las condiciones en las que vive.</p>
            <p>El bienestar de un animal puede describirse como bueno si el individuo está en buenas condiciones, saludable y libre de sufrimiento.</p>
            <p>Debemos evaluar el bienestar de los animales usando las “Cinco Libertades” (Concejo sobre Bienestar en Animales de Granja, 2003):</p>
            <ul class="info">
                <li>Libertad de hambre y sed.</li>
                <li>Libertad de incomodidad.</li>
                <li>Libertad de dolor, lesiones y enfermedades.</li>
                <li>Libertad de miedo y angustia.</li>
                <li>Libertad para expresar comportamientos normales.</li>
            </ul>
            <p>Las anteriores representan una “lista de control” útil para identificar rápidamente situaciones que comprometan el bienestar animal, es decir, cualquier situación que cause miedo, dolor, incomodidad, daño, enfermedad o angustia en relación al comportamiento.</p>
            <p>Los métodos para dar muerte a los animales son críticos ya que a menudo el proceso puede causar dolor y angustia extremos antes de que ocurra la muerte. La Fundación abogará por métodos de sacrificio humanitarios.</p>
